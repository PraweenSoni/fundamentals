# Math: Arithetic Operators
=begin
    + addition
    - subtraction
    * multiplication
    / devision
    % module (remainder)
    ** exponential (Power)
=end

a = 7;
b = 2;

puts a + b
puts a - b
puts a * b
puts a % b
# output : 1
puts a / b
# output : 3
puts a ** b
# output : 49

c = 3.14  # decimal output because c datatype is decimal
puts c / b
# output : 1.57